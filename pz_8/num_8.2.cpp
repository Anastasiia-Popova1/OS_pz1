// Завдання 2. Реалізувати приорітетний алгоритм планування.  Хай: є 4 черги (Q1, Q2, Q3, Q4.)
// для процесів, Приорітети черг: Pr(Q1) > Pr(Q2)> Pr(Q3)> Pr(Q4). Проміжок кванту: 
// T(Q1)< T(Q2)< T(Q3)< T(Q4) = ∞. Після запуску процес записується в чергу Q1. 
// Післчя закінчення кванту часу, якщо процес не завершено, він записується в наступну чергу. 
// Процес залишається в черзі Q4 до повного завершення. Для виділення кванту часу обирається 
// черга з найвищим приорітетом.
 

#include <iostream>
#include <queue>    

using namespace std; 

class Process { 
public: 
    int pid; 
    int remaining_time;     //залишок часу, який ще потрібно виконати
    int original_time;   //початковий час виконання 
    
    Process(int id, int time)  //конструктор
        : pid(id),            
          remaining_time(time), 
          original_time(time) {} 
    
    void print() const { //вивід інформації про процес
        cout << "PID=" << pid  
             << ", залишок=" << remaining_time 
             << "/" << original_time; 
    }
};

class PriorityQueue { //клас для черги з пріоритетом 
private: 
    queue<Process> q; //черга FIFO для зберігання процесів
    string name;   
    
public:
    PriorityQueue(const string& n) : name(n) {} //запам'ятовуємо назву черги
    
    void enqueue(const Process& p) { 
        q.push(p); //додаємо в кінець черги
        cout << "  Процес PID=" << p.pid << " додано до черги " << name << endl; 
    }
    
    bool dequeue(Process& p) { //вийняти процес з черги 
        if (q.empty()) return false;
        p = q.front();      //копіюємо перший процес
        q.pop();         //видаляємо
        return true;   
    }
    
    bool isEmpty() const { 
        return q.empty();
    }
    
    string getName() const { 
        return name;
    }
    
    int getSize() const { 
        return q.size();
    }
};

class MLFQScheduler { //клас планувальника MLFQ 
private: // Приватні члени
    PriorityQueue Q1, Q2, Q3, Q4; 
    int T1, T2, T3; 
    
    PriorityQueue* selectHighestPriorityQueue() {           //вибрати чергу з найвищим пріоритетом
        if (!Q1.isEmpty()) return &Q1; // Q1 має найвищий пріоритет
        if (!Q2.isEmpty()) return &Q2; // Q2 - середній
        if (!Q3.isEmpty()) return &Q3; // Q3 - низький
        if (!Q4.isEmpty()) return &Q4; // Q4 - найнижчий 
        return nullptr; 
    }
    
    int getTimeSlice(PriorityQueue* q) {    //визначити квант часу для черги
        if (q == &Q1) return T1;     // Q1 -> квант 2
        if (q == &Q2) return T2;      // Q2 -> квант 4
        if (q == &Q3) return T3;    // Q3 -> квант 8
        return -1; 
    }
    
    PriorityQueue* getNextQueue(PriorityQueue* current_q) {         //визначити наступну чергу при пониженні
        if (current_q == &Q1) return &Q2; // З Q1 -> в Q2
        if (current_q == &Q2) return &Q3; // З Q2 -> в Q3
        if (current_q == &Q3) return &Q4; // З Q3 -> в Q4
        return nullptr; 
    }
    
public:
    MLFQScheduler(int t1 = 2, int t2 = 4, int t3 = 8)
        : Q1("Q1(пріоритет=високий)"),   
          Q2("Q2(пріоритет=середній)"), 
          Q3("Q3(пріоритет=низький)"),  
          Q4("Q4(пріоритет=найнижчий, FCFS)"), 
          T1(t1), T2(t2), T3(t3) {} 
    
    void addProcess(int pid, int time) {        //додати новий процес
        Process p(pid, time);
        cout << "\nНовий процес ";
        p.print(); 
        cout << " -> додається в Q1" << endl; 
        Q1.enqueue(p);      //додаємо процес до черги Q1
    }
    
    void run() { 
        cout << "\nПланувальник\n";
        cout << "Кванти: Q1=" << T1 << ", Q2=" << T2 << ", Q3=" << T3 << ", Q4=∞\n\n"; 
        
        int step = 1; 
        
        while (true) { 
            PriorityQueue* current_q = selectHighestPriorityQueue();        //вибираємо чергу з найвищим пріоритетом
            if (current_q == nullptr) {             //якщо всі черги порожні
                cout << "\nУсі черги порожні. Планування завершено\n"; 
                break; 
            }
            
            int time_slice = getTimeSlice(current_q);       //отримуємо квант часу для цієї черги
            Process current_process(0, 0);      // Тимчасовий об'єкт для процесу
            
            if (!current_q->dequeue(current_process)) continue;         //якщо не вдалося взяти процес - пропускаємо
            
            cout << "Крок " << step++ << endl; 
            cout << "Вибрано чергу: " << current_q->getName() << endl; 
            cout << "Виконується процес: "; 
            current_process.print(); 
            
            if (time_slice == -1) {         //якщо квант = ∞ 
                cout << ", отримав квант = ∞ (до завершення)" << endl; 
                cout << "Процес PID=" << current_process.pid << " завершено" << endl; 
                continue; 
            } else {
                cout << ", отримав квант = " << time_slice << endl; 
            }
            
            if (current_process.remaining_time <= time_slice) { //якщо процес вміщується в квант
                cout << "  Процес завершився за " << current_process.remaining_time  
                     << " од. часу (квант не повністю використано)" << endl; 
                cout << "Процес PID=" << current_process.pid << " завершено" << endl; 
            } else {          
                current_process.remaining_time -= time_slice;        //зменшуємо залишок часу на квант
                PriorityQueue* next_q = getNextQueue(current_q);        //визначаємо наступну чергу 
                
                cout << "  Використано повний квант = " << time_slice << endl; 
                cout << "  Процес не завершено, залишок = " << current_process.remaining_time << endl; 
                
                if (next_q != nullptr) {        //якщо є куди перемістити
                    cout << "  Переміщення процесу в " << next_q->getName() << endl; 
                    next_q->enqueue(current_process);       //додаємо процес до наступної черги
                }
            }
            
            printQueueState(); 
        }
    }
    
    void printQueueState() const {
        cout << "\nСтан\n"; 
        cout << "Q1 (високий пріоритет, квант=" << T1 << "): " << Q1.getSize() << " процесів\n"; //стан Q1
        cout << "Q2 (середній пріоритет, квант=" << T2 << "): " << Q2.getSize() << " процесів\n"; //Q2
        cout << "Q3 (низький пріоритет, квант=" << T3 << "): " << Q3.getSize() << " процесів\n"; //Q3
        cout << "Q4 (найнижчий пріоритет, FCFS): " << Q4.getSize() << " процесів\n"; //Q4
    }
};

int main() { 
    MLFQScheduler scheduler(2, 4, 8); 
    
    scheduler.addProcess(1, 10); 
    scheduler.addProcess(2, 5);  
    scheduler.addProcess(3, 8);  
    
    cout << "\n";
    scheduler.run();
    
    return 0; 
}

